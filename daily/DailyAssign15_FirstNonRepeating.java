package dailyassignment;

public class DailyAssign15_FirstNonRepeating {
       public static void main(String[] args) {
		
    	   int arr[] = {4,5,1,2,0,4};
    	   
    	   for(int i=0;i<=arr.length-1;i++) {
    		   
    		   if(arr[i]==arr[i+1]) {
        		     continue;
        	   }
    		   
    		   
    	   }
    	   
    	   
	}
}
